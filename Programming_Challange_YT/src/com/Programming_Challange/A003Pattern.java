package com.Programming_Challange;

public class A003Pattern {
	public static void main(String[] args) {
		// Pattern 1
		System.out.println("*");
		System.out.println("* *");
		System.out.println("* * *");
		System.out.println("* * * *");
		System.out.println("* * * * *");
		System.out.println();
		// Pattern 2
		System.out.println("* * * * *");
		System.out.println("* * * *");
		System.out.println("* * *");
		System.out.println("* *");
		System.out.println("*");
		System.out.println();
		// Pattern 3
		System.out.println("        *");
		System.out.println("      * *");
		System.out.println("    * * *");
		System.out.println("  * * * *");
		System.out.println("* * * * *");
	}

}
